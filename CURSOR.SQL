DECLARE
    FNAME VARCHAR(200);
    CURSOR FIRST_CURSOR
    CURSOR FIRST_CURSOR IS
    SELECT * INTO FNAME FROM STUDENTS;
BEGIN
    OPEN FIRST_CURSOR;
    LOOP
        FETCH FIRST_CURSOR INTO FNAME;
        DBMS_OUTPUT.PUT_LINE(FNAME);
        EXIT WHEN FIRST_CURSOR%NOTFOUND;
    END LOOP;
    CLOSE FIRST_CURSOR;
END;
