DECLARE
    FNAME VARCHAR(200);
    
    CURSOR FIRST_CURSOR(NAME VARCHAR) IS
    SELECT * INTO FNAME FROM STUDENTS
    WHERE FNAME = NAME;
BEGIN
    OPEN FIRST_CURSOR('GIR');
    LOOP
        FETCH FIRST_CURSOR INTO FNAME;
        DBMS_OUTPUT.PUT_LINE(FNAME);
        EXIT WHEN FIRST_CURSOR%NOTFOUND;
    END LOOP;
    CLOSE FIRST_CURSOR;
END;
